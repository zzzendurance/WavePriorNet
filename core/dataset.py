"""
REVIDE_indoor Dataset Loader for WavePriorNet.

Directory layout expected:
    <data_root>/
        Train/
            hazy/<seq_name>/<frame>.JPG
            gt/<seq_name>/<frame>.JPG
            gt_color_shift/<seq_name>/<frame>.JPG   (generated by color_shift.py)
        Test/
            hazy/<seq_name>/<frame>.JPG
            gt/<seq_name>/<frame>.JPG
            gt_color_shift/<seq_name>/<frame>.JPG

Each sample returns T consecutive frames for all three modalities:
    hazy   [T, 3, H, W]  – degraded input
    color  [T, 3, H, W]  – color-shifted GT (color reference)
    gt     [T, 3, H, W]  – clean ground truth
"""

import os
import random
from pathlib import Path

import torch
from torch.utils.data import Dataset
from torchvision import transforms
from torchvision.transforms import functional as TF
from PIL import Image


IMG_EXTENSIONS = {'.jpg', '.jpeg', '.png', '.bmp', '.JPG', '.JPEG', '.PNG'}


def _list_frames(seq_dir: Path):
    frames = sorted([p for p in seq_dir.iterdir() if p.suffix in IMG_EXTENSIONS])
    return frames


class REVIDEDataset(Dataset):
    """
    Args:
        data_root: path to REVIDE_indoor/
        split: 'Train' or 'Test'
        num_frames: number of consecutive frames per sample (T)
        crop_size: (H, W) for random crop during training; None = no crop
        augment: apply random flip/crop (training only)
    """

    def __init__(
        self,
        data_root: str,
        split: str = 'Train',
        num_frames: int = 5,
        crop_size=(256, 256),
        augment: bool = True,
    ):
        super().__init__()
        self.split = split
        self.num_frames = num_frames
        self.crop_size = crop_size
        self.augment = augment and (split == 'Train')

        root = Path(data_root) / split
        hazy_root = root / 'hazy'
        gt_root = root / 'gt'
        color_root = root / 'gt_color_shift'

        if not hazy_root.exists():
            raise FileNotFoundError(f"hazy folder not found: {hazy_root}")
        if not gt_root.exists():
            raise FileNotFoundError(f"gt folder not found: {gt_root}")
        if not color_root.exists():
            raise FileNotFoundError(
                f"gt_color_shift folder not found: {color_root}\n"
                "Run: python color_shift.py --split {split} --data_dir <data_root>"
            )

        seq_names = sorted([d.name for d in hazy_root.iterdir() if d.is_dir()])

        self.samples = []  # list of (hazy_frames, color_frames, gt_frames, start_idx)
        for seq in seq_names:
            hazy_frames = _list_frames(hazy_root / seq)
            gt_frames = _list_frames(gt_root / seq)
            color_frames = _list_frames(color_root / seq)

            n = min(len(hazy_frames), len(gt_frames), len(color_frames))
            if n < num_frames:
                continue

            if split == 'Train':
                # all valid starting positions
                for start in range(n - num_frames + 1):
                    self.samples.append((hazy_frames, color_frames, gt_frames, start))
            else:
                # test: non-overlapping clips
                for start in range(0, n - num_frames + 1, num_frames):
                    self.samples.append((hazy_frames, color_frames, gt_frames, start))

        if len(self.samples) == 0:
            raise RuntimeError(f"No valid samples found in {root}")

    def __len__(self):
        return len(self.samples)

    def _load_img(self, path: Path) -> Image.Image:
        return Image.open(path).convert('RGB')

    def __getitem__(self, idx):
        hazy_paths, color_paths, gt_paths, start = self.samples[idx]

        hazy_imgs = [self._load_img(hazy_paths[start + i]) for i in range(self.num_frames)]
        color_imgs = [self._load_img(color_paths[start + i]) for i in range(self.num_frames)]
        gt_imgs = [self._load_img(gt_paths[start + i]) for i in range(self.num_frames)]

        # --- spatial augmentation (same transform for all frames & modalities) ---
        if self.augment and self.crop_size is not None:
            # random crop params
            w, h = hazy_imgs[0].size
            ch, cw = self.crop_size
            top = random.randint(0, h - ch)
            left = random.randint(0, w - cw)

            def crop(img):
                return TF.crop(img, top, left, ch, cw)

            hazy_imgs = [crop(f) for f in hazy_imgs]
            color_imgs = [crop(f) for f in color_imgs]
            gt_imgs = [crop(f) for f in gt_imgs]

            # random horizontal flip
            if random.random() > 0.5:
                hazy_imgs = [TF.hflip(f) for f in hazy_imgs]
                color_imgs = [TF.hflip(f) for f in color_imgs]
                gt_imgs = [TF.hflip(f) for f in gt_imgs]

            # random vertical flip
            if random.random() > 0.5:
                hazy_imgs = [TF.vflip(f) for f in hazy_imgs]
                color_imgs = [TF.vflip(f) for f in color_imgs]
                gt_imgs = [TF.vflip(f) for f in gt_imgs]

        elif self.crop_size is not None and not self.augment:
            # center crop for test
            def ccrop(img):
                return TF.center_crop(img, self.crop_size)
            hazy_imgs = [ccrop(f) for f in hazy_imgs]
            color_imgs = [ccrop(f) for f in color_imgs]
            gt_imgs = [ccrop(f) for f in gt_imgs]

        to_tensor = transforms.ToTensor()  # [0,1] float32

        hazy = torch.stack([to_tensor(f) for f in hazy_imgs])    # [T,3,H,W]
        color = torch.stack([to_tensor(f) for f in color_imgs])  # [T,3,H,W]
        gt = torch.stack([to_tensor(f) for f in gt_imgs])        # [T,3,H,W]

        return hazy, color, gt
